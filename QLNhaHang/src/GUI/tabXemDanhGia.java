    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        String txtTimKiem = cbNoiDungTimKiem.getSelectedItem().toString();
        String noiDungTK = "";
        TableRowSorter sorter = new TableRowSorter(tableModel);
        tbHienThiDanhGia.setRowSorter(sorter);
        if(txtTimKiem.equals("Tìm kiếm theo món ăn")){
            if(cbMonAn.getSelectedItem().toString().trim().equals("Chọn món ăn")){
                JOptionPane.showMessageDialog(this, "Vui lòng chọn món ăn để tìm kiếm");
                return;
            }
            noiDungTK = cbMonAn.getSelectedItem().toString();
            sorter.setRowFilter(RowFilter.regexFilter("^" + Pattern.quote(noiDungTK)+ "$", 1));
        }

        else if(txtTimKiem.equals("Tìm kiếm theo đánh giá")){
            if(cbDiemDanhGia.getSelectedItem().toString().equals("Chọn đánh giá")){
                JOptionPane.showMessageDialog(this, "Vui lòng chọn đánh giá để tìm kiếm");
                return;
            }
            noiDungTK = cbDiemDanhGia.getSelectedItem().toString();
            sorter.setRowFilter(RowFilter.regexFilter("^" + Pattern.quote(noiDungTK)+ "$", 4));
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

