    public void layDuLieu(){
        tableModel = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; 
            }
        };
        tableModel.addColumn("Tên khách hàng");
        tableModel.addColumn("Tên món ăn");
        tableModel.addColumn("Nội dung đánh giá");
        tableModel.addColumn("Thời gian");
        tableModel.addColumn("Điểm");
        cbNoiDungTimKiem.removeAllItems();
        cbNoiDungTimKiem.addItem("Tìm kiếm theo món ăn");
        cbNoiDungTimKiem.addItem("Tìm kiếm theo đánh giá");
        cbMonAn.removeAllItems();
        cbMonAn.addItem("Chọn món ăn");
        cbDiemDanhGia.removeAllItems();
        cbDiemDanhGia.addItem("Chọn đánh giá");
        for(int i = 1; i <= 5; i++){
            cbDiemDanhGia.addItem(String.valueOf(i));
        }
        for(DanhGia dg: layDuLieu.getDsDanhGia()){
            String ten = "";
            for(KhachHang kh: layDuLieu.getDsKhachHang()){
                if(dg.getMaKhachHang() == kh.getMaKhachHang()){
                    ten = kh.getTenKhachHang();
                    break;
                }
            }
            String monAn = "";
            for(MonAn mon: layDuLieu.getDsMonAn()){
                if(dg.getMaMonAn() == mon.getMaMonAn()){
                    monAn = mon.getTenMon();
                }
            }
            cbMonAn.addItem(monAn);
            tableModel.addRow(new Object[]{
                ten,
                monAn,
                dg.getNoiDungDanhGia(),
                dg.getThoiGianDanhGia(),
                dg.getDiemDanhGia()
            });
        }
        tbHienThiDanhGia.setModel(tableModel);
    }
